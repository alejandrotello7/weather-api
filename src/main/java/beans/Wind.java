package beans;

public class Wind {
	private static String speed;
	private static String deg;
	public static String getSpeed() {
		return speed;
	}
	public static void setSpeed(String speed) {
		Wind.speed = speed;
	}
	public static String getDeg() {
		return deg;
	}
	public static void setDeg(String deg) {
		Wind.deg = deg;
	}

}
